double sum(double a, double b)
{
    double z = (a + b);
    return z;
}
double subt(double a, double b)
{
    double sub = (a - b);
    return sub;
}
double multiply(double a, double b)
{
    double result = (a * b);
    return result;
}
double divide(double a, double b)
{
    double div = (a / b);
    return div;
}
double average(double a, double b)
{
    double ave = (a / 100)* b;
    return ave;

}
